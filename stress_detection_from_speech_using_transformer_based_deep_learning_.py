from datasets import Dataset

def prepare_dataset(dataframe):

    audio_list = []
    label_list = []

    for _, row in dataframe.iterrows():

        speech = load_audio(
            row['audio_path']
        )

        audio_list.append(speech)

        label_list.append(
            int(row['label'])
        )

    return Dataset.from_dict(
        {
            "audio": audio_list,
            "label": label_list
        }
    )

