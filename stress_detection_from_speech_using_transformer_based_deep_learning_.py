import numpy as np

def compute_metrics(pred):

    labels = pred.label_ids

    preds = np.argmax(
        pred.predictions,
        axis=1
    )

    return {

        "accuracy":
        accuracy_score(labels,preds),

        "precision":
        precision_score(labels,preds),

        "recall":
        recall_score(labels,preds),

        "f1":
        f1_score(labels,preds)
    }

